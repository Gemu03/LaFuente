/* Carrusel*/
const body = document.body
const slides = document.querySelectorAll('.carrousel')
const leftBtn = document.getElementById('left')
const rightBtn = document.getElementById('right')

let activeSlide = 0

rightBtn.addEventListener('click', () => {
  activeSlide++

  if (activeSlide > slides.length - 1) {
    activeSlide = 0
  }

  setBgToBody()
  setActiveSlide()
})

leftBtn.addEventListener('click', () => {
  activeSlide--

  if (activeSlide < 0) {
    activeSlide = slides.length - 1
  }

  setBgToBody()
  setActiveSlide()
})

setBgToBody()

function setBgToBody() {
  body.style.backgroundImage = slides[activeSlide].style.backgroundImage
}

function setActiveSlide() {
  slides.forEach((slide) => slide.classList.remove('active'))

  slides[activeSlide].classList.add('active')
}

















/*Carrusel Start*/
let slider = document.querySelector('.slider .list');
let items = document.querySelectorAll('.slider .list .item');
let next = document.getElementById('next');
let prev = document.getElementById('prev');
let dots = document.querySelectorAll('.slider .dots li');

let lengthItems = items.length - 1;
let active = 0;
next.onclick = function(){
    active = active + 1 <= lengthItems ? active + 1 : 0;
    reloadSlider();
}
prev.onclick = function(){
    active = active - 1 >= 0 ? active - 1 : lengthItems;
    reloadSlider();
}
let refreshInterval = setInterval(()=> {next.click()}, 3000);
function reloadSlider(){
    slider.style.left = -items[active].offsetLeft + 'px';
    // 
    let last_active_dot = document.querySelector('.slider .dots li.active');
    last_active_dot.classList.remove('active');
    dots[active].classList.add('active');

    clearInterval(refreshInterval);
    refreshInterval = setInterval(()=> {next.click()}, 3000); 
}

dots.forEach((li, key) => {
    li.addEventListener('click', ()=>{
         active = key;
         reloadSlider();
    })
})
window.onresize = function(event) {
    reloadSlider();
};
/*Carrusel end*/




















$(document).ready(function() {
  function cloneSlides() {
    const $slider = $('.slider');
    const $slideTrack1 = $slider.find('.slide-track1');
    const $slideTrack2 = $slider.find('.slide-track2');
    $slideTrack1.append($slideTrack1.html());
    $slideTrack2.append($slideTrack2.html());
  }

  cloneSlides();

  // Espera a que la clonación se complete antes de iniciar la animación
  setTimeout(function() {
    const $slider = $('.slider');
    const $slideTrack1 = $slider.find('.slide-track1');
    const $slideTrack2 = $slider.find('.slide-track2');
    $slideTrack1.css('animation', 'scroll-up 10s linear infinite');
    $slideTrack2.css('animation', 'scroll-down 10s linear infinite');
  }, 0);
});





